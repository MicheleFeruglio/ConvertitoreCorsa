package convertitore;

public class Dati {

    private double distanza;
    private int ore;
    private int minuti;
    private int secondi;
    private int passoMinuti;
    private int passoSecondi;
    private double velocita;

    public double getDistanza () {
        return distanza;
    }

    public void setDistanza (final double distanza) {
        this.distanza = distanza;
    }

    public int getOre () {
        return ore;
    }

    public void setOre (final int ore) {
        this.ore = ore;
    }

    public int getMinuti () {
        return minuti;
    }

    public void setMinuti (final int minuti) {
        this.minuti = minuti;
    }

    public int getSecondi () {
        return secondi;
    }

    public void setSecondi (final int secondi) {
        this.secondi = secondi;
    }

    public int getPassoMinuti () {
        return passoMinuti;
    }

    public void setPassoMinuti (final int passoMinuti) {
        this.passoMinuti = passoMinuti;
    }

    public int getPassoSecondi () {
        return passoSecondi;
    }

    public void setPassoSecondi (final int passoSecondi) {
        this.passoSecondi = passoSecondi;
    }

    public double getVelocita () {
        return velocita;
    }

    public void setVelocita (final double velocita) {
        this.velocita = velocita;
    }
}
